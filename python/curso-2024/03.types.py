


# Exemplos de tipos de dados em Python

# Inteiro (int): Representa números inteiros.
numero_inteiro = 42
print("Número inteiro:", numero_inteiro)

# Flutuante (float): Representa números de ponto flutuante.
numero_flutuante = 3.14
print("Número flutuante:", numero_flutuante)

# String (str): Representa texto.
texto = "Olá, mundo!"
print("Texto:", texto)

# Booleano (bool): Representa valores booleanos verdadeiro ou falso.
verdadeiro = True
falso = False
print("Booleano verdadeiro:", verdadeiro)
print("Booleano falso:", falso)

# Lista (list): Uma coleção ordenada de elementos.
lista = [1, 2, 3, 4, 5]
print("Lista:", lista)

# Tupla (tuple): Uma coleção ordenada e imutável de elementos.
tupla = (10, 20, 30)
print("Tupla:", tupla)

# Dicionário (dict): Uma coleção não ordenada de pares chave-valor.
dicionario = {'chave': 'valor', 'nome': 'João', 'idade': 30}
print("Dicionário:", dicionario)

# Conjunto (set): Uma coleção não ordenada de elementos únicos.
conjunto = {1, 2, 3, 4, 5}
print("Conjunto:", conjunto)

# Nenhum (NoneType): Representa a ausência de valor.
nenhum_valor = None
print("Nenhum valor:", nenhum_valor)
