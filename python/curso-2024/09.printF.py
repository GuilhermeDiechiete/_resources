
# Definindo variáveis
nome = "Alice"
idade = 30

# Criando uma f-string para incluir variáveis diretamente na string
mensagem = f"Olá, meu nome é {nome} e eu tenho {idade} anos."
print(mensagem)  # Saída: Olá, meu nome é Alice e eu tenho 30 anos.


# Definindo variáveis
a = 10
b = 20

# Realizando cálculos dentro de uma f-string
resultado = f"A soma de {a} e {b} é {a + b}."
print(resultado)  # Saída: A soma de 10 e 20 é 30.



# Definindo variável
numero = 12345.6789

# Formatando números dentro de uma f-string
formatado = f"O número é {numero:.2f}"
print(formatado)  # Saída: O número é 12345.68



# Definindo variável
nome = "joão"

# Chamando métodos dentro de uma f-string
formatado = f"O nome formatado é {nome.capitalize()}"
print(formatado)  # Saída: O nome formatado é João
