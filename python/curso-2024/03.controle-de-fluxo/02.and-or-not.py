

# E lógico (and): Retorna True se ambas as expressões forem verdadeiras.
x = 5
print(x > 0 and x < 10)  # Verifica se x é maior que 0 E menor que 10. Saída: True

# Ou lógico (or): Retorna True se pelo menos uma das expressões for verdadeira.
x = 5
print(x < 0 or x > 10)  # Verifica se x é menor que 0 OU maior que 10. Saída: True

# Não lógico (not): Inverte o valor de uma expressão.
x = 5
print(not(x > 0 and x < 10))  # Verifica se NÃO (x é maior que 0 E menor que 10). Saída: False
