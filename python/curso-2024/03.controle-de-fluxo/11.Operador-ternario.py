
# Ternary Operator ( Operador Ternário )

age = 20

resultado = 'Voto Permitido' if age >= 16 else 'Voto não Permitido'
print(resultado)

# Define o número que será verificado se é par ou ímpar
numero = 10

# Usa o operador ternário para verificar se o número é par ou ímpar
# Se o resto da divisão do número por 2 for zero, significa que é par, então atribui "Par" à variável mensagem
# Caso contrário, significa que é ímpar, então atribui "Ímpar" à variável mensagem
mensagem = "Par" if numero % 2 == 0 else "Ímpar"

# Imprime a mensagem correspondente ao número
print(mensagem)  # Saída: Par
