
# Quando precisar verificar se uma string começa ou termina com alguns caracteres, pode usar os metódos startwidth e endswith.

name = "Guilherme Diechiete da Silva"

# verificar string do inicio ( case )
print(name.startswith('Guilherme'))

# verificar string no final ( case )
print(name.endswith('Silva'))


# verificar se uma palavra existe
print("Diechiete" in name)
