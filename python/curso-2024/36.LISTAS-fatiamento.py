
# Definindo a lista
lista = [1, 2, 3, 4, 5, 6]


# Imprimindo os elementos da lista do índice 0 até o índice 4 (exclusivo)
print(lista[0:5])  # Saída: [1, 2, 3, 4, 5]

# Imprimindo os elementos da lista do início até o índice 4 (exclusivo)
print(lista[:5])  # Saída: [1, 2, 3, 4, 5]

# Imprimindo os elementos da lista do início até o penúltimo elemento
print(lista[:-1])  # Saída: [1, 2, 3, 4, 5]

# Imprimindo os elementos da lista do índice 1 até o índice 2 (exclusivo)
print(lista[1:3])  # Saída: [2, 3]

# Imprimindo os elementos da lista do índice 3 até o último elemento
print(lista[3:])  # Saída: [4, 5, 6]

# Imprimindo os três primeiros elementos da lista
print(lista[:3])  # Saída: [1, 2, 3]

# Imprimindo o último elemento da lista
print(lista[-1])  # Saída: 6
