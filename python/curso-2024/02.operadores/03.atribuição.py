

# Atribuição simples (=): Atribui o valor à direita à variável à esquerda.
x = 5
print(x)  # Saída: 5

# Atribuição com adição (+=): Adiciona o valor à direita ao valor atual da variável e atribui o resultado à variável.
x = 5
x += 3  # Equivalente a x = x + 3
print(x)  # Saída: 8

# Atribuição com subtração (-=): Subtrai o valor à direita do valor atual da variável e atribui o resultado à variável.
x = 5
x -= 2  # Equivalente a x = x - 2
print(x)  # Saída: 3

# Atribuição com multiplicação (*=): Multiplica o valor atual da variável pelo valor à direita e atribui o resultado à variável.
x = 5
x *= 2  # Equivalente a x = x * 2
print(x)  # Saída: 10

# Atribuição com divisão (/=): Divide o valor atual da variável pelo valor à direita e atribui o resultado à variável.
x = 10
x /= 2  # Equivalente a x = x / 2
print(x)  # Saída: 5.0

# Atribuição com resto (%=): Divide o valor atual da variável pelo valor à direita e atribui o resto à variável.
x = 10
x %= 3  # Equivalente a x = x % 3
print(x)  # Saída: 1

# Atribuição com exponenciação (**=): Eleva o valor atual da variável à potência do valor à direita e atribui o resultado à variável.
x = 2
x **= 3  # Equivalente a x = x ** 3
print(x)  # Saída: 8

# Atribuição com divisão inteira (//=): Divide o valor atual da variável pelo valor à direita e atribui a parte inteira do resultado à variável.
x = 10
x //= 3  # Equivalente a x = x // 3
print(x)  # Saída: 3
