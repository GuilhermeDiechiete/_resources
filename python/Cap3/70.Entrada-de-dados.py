# CONVERSOES

# Conversão para Inteiro (int):
numero_str = "10"  # Definindo uma string contendo um número
numero_int = int(numero_str)  # Convertendo a string para um inteiro
print(numero_int)  # Saída: 10

# Conversão para Ponto Flutuante (float):
numero_str = "3.14"  # Definindo uma string contendo um número de ponto flutuante
numero_float = float(numero_str)  # Convertendo a string para um ponto flutuante
print(numero_float)  # Saída: 3.14

# Conversão para String (str):
numero_int = 10  # Definindo um número inteiro
numero_str = str(numero_int)  # Convertendo o número para uma string
print(numero_str)  # Saída: '10'

# Conversão para Booleano (bool):
valor = 0  # Definindo um valor
valor_bool = bool(valor)  # Convertendo o valor para um booleano
print(valor_bool)  # Saída: False

