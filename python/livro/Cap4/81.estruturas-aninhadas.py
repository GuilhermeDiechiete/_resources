# Definindo uma variável
idade = 20

# Verificando se a idade é maior ou igual a 18
if idade >= 18:
    print("Você é maior de idade.")
    # Verificando se a idade é menor que 65
    if idade < 65:
        print("Você ainda não é elegível para aposentadoria.")
    else:
        print("Você já é elegível para aposentadoria.")
else:
    print("Você é menor de idade.")
