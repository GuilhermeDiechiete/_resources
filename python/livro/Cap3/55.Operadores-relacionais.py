
# Operadores Relacionais

# Igual a (==)
print(5 == 5)  # True
print(10 == 5)  # False

# Diferente de (!=)
print(5 != 10)  # True
print(5 != 5)   # False

# Maior que (>)
print(10 > 5)   # True
print(5 > 10)   # False

# Menor que (<)
print(5 < 10)   # True
print(10 < 5)   # False

# Maior ou igual a (>=)
print(10 >= 5)  # True
print(5 >= 10)  # False
print(5 >= 5)   # True

# Menor ou igual a (<=)
print(5 <= 10)  # True
print(10 <= 5)  # False
print(5 <= 5)   # True
