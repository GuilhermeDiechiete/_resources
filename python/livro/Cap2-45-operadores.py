# Operadores aritméticos
x = 10
y = 3

# Adição
soma = x + y  
print("Adição:", soma)  # Saída: Adição: 13

# Subtração
subtracao = x - y  
print("Subtração:", subtracao)  # Saída: Subtração: 7

# Multiplicação
multiplicacao = x * y  
print("Multiplicação:", multiplicacao)  # Saída: Multiplicação: 30

# Divisão
divisao = x / y  
print("Divisão:", divisao)  # Saída: Divisão: 3.3333333333333335

# Resto da divisão (Módulo)
modulo = x % y  
print("Módulo:", modulo)  # Saída: Módulo: 1

# Exponenciação
exponenciacao = x ** y  
print("Exponenciação:", exponenciacao)  # Saída: Exponenciação: 1000

# Divisão inteira
divisao_inteira = x // y  
print("Divisão Inteira:", divisao_inteira)  # Saída: Divisão Inteira: 3

# Operadores de comparação
a = 5
b = 10

# Igualdade
igualdade = (a == b)  
print("Igualdade:", igualdade)  # Saída: Igualdade: False

# Diferença
diferenca = (a != b)  
print("Diferença:", diferenca)  # Saída: Diferença: True

# Menor que
menor_que = (a < b)  
print("Menor que:", menor_que)  # Saída: Menor que: True

# Maior que
maior_que = (a > b)  
print("Maior que:", maior_que)  # Saída: Maior que: False

# Menor ou igual a
menor_ou_igual_a = (a <= b)  
print("Menor ou igual a:", menor_ou_igual_a)  # Saída: Menor ou igual a: True

# Maior ou igual a
maior_ou_igual_a = (a >= b)  
print("Maior ou igual a:", maior_ou_igual_a)  # Saída: Maior ou igual a: False

# Operadores lógicos
p = True
q = False

# Operador lógico E (and)
logico_e = p and q  
print("Operador lógico E (and):", logico_e)  # Saída: Operador lógico E (and): False

# Operador lógico OU (or)
logico_ou = p or q  
print("Operador lógico OU (or):", logico_ou)  # Saída: Operador lógico OU (or): True

# Operador lógico NÃO (not)
logico_nao = not p  
print("Operador lógico NÃO (not):", logico_nao)  # Saída: Operador lógico NÃO (not): False

# Operadores de associação e identidade
lista = [1, 2, 3]

# Pertence a (para sequências)
pertence_a = 2 in lista  
print("Pertence a (para sequências):", pertence_a)  # Saída: Pertence a (para sequências): True

# Não pertence a (para sequências)
nao_pertence_a = 4 not in lista  
print("Não pertence a (para sequências):", nao_pertence_a)  # Saída: Não pertence a (para sequências): True

# É o mesmo objeto (identidade do objeto)
x = 5
y = x
mesmo_objeto = x is y  
print("É o mesmo objeto (identidade do objeto):", mesmo_objeto)  # Saída: É o mesmo objeto (identidade do objeto): True

# Não é o mesmo objeto (identidade do objeto)
z = 5
diferente_objeto = x is not z  
print("Não é o mesmo objeto (identidade do objeto):", diferente_objeto)  # Saída: Não é o mesmo objeto (identidade do objeto): False
