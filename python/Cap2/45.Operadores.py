a = 10
b = 3

# Adição
resultado_adicao = a + b
print("Adição:", resultado_adicao)  # Adição: 13

# Subtração
resultado_subtracao = a - b
print("Subtração:", resultado_subtracao)  # Subtração: 7

# Multiplicação
resultado_multiplicacao = a * b
print("Multiplicação:", resultado_multiplicacao)  # Multiplicação: 30

# Divisão
resultado_divisao = a / b
print("Divisão:", resultado_divisao)  # Divisão: 3.3333333333333335

# Divisão inteira
resultado_divisao_inteira = a // b
print("Divisão inteira:", resultado_divisao_inteira)  # Divisão inteira: 3

# Módulo
resultado_modulo = a % b
print("Módulo:", resultado_modulo)  # Módulo: 1

# Exponenciação
resultado_exponenciacao = a ** b
print("Exponenciação:", resultado_exponenciacao)  # Exponenciação: 1000
