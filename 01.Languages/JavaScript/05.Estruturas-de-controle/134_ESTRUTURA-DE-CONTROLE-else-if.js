function retornarSabor(codigo){
    if(codigo == 1){
        console.log('Nutella')
    } else if(codigo ==2){
        console.log('Chocolate')
    } else if(codigo == 3){
        console.log('Banana')
    } else {
        console.log('Codigo Invalido')
    }
}

retornarSabor(1)
retornarSabor(6)
retornarSabor(2)
retornarSabor(3)
retornarSabor(5)